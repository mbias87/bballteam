

 namespace BballTeam 
{
    public class PlayerStats{

        /// <summary>
        ///  a players points per game
        /// </summary>
        /// <value></value>
        public double Points {get; set;}
        /// <summary>
        /// a players rebounds per game
        /// </summary>
        /// <value></value>
        public double Rebounds {get; set;}

        /// <summary>
        /// a players assists per game
        /// </summary>
        /// <value></value>
        public double Assists {get; set;}

        /// <summary>
        /// a players blocks per game
        /// </summary>
        /// <value></value>
        public double Blocks {get; set;}

        /// <summary>
        /// a players steal per game
        /// </summary>
        /// <value></value>
        public double Steals {get; set;}

        /// <summary>
        /// a players turn overs
        /// </summary>
        /// <value></value>
        public double Turnovers {get; set;}
    }
}