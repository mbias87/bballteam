
 namespace BballTeam
 {
     public class Player {
         /// <summary>
         /// a basketball player position
         /// </summary>
         public string Position {get; set;}
         /// <summary>
         /// a players name
         /// </summary>
         public string Name {get; set;}
         /// <summary>
         /// determines whether a player is a starter
         /// </summary>
         public bool Starter {get; set;}

        /// <summary>
        /// a players height
        /// </summary>
        public string Height {get; set;}
        /// <summary>
        /// a players weight
        /// </summary>
        public string Weight {get; set;}
        /// <summary>
        ///  a players country
        /// </summary>
        /// 
        public string Country {get; set;}
        /// <summary>
        /// the college a player attended
        /// </summary>
        public string College {get; set;}
        /// <summary>
        /// a players Age
        /// </summary>
        public int Age {get; set;}

        /// <summary>
        /// a players birthdate 
        /// </summary>
        public string BirthDate {get; set;}
     }
 }